import pandas as pd
from pandasql import sqldf

# 1. Load the uploaded file into a DataFrame
df = pd.read_csv('application_train.csv')

# 2. Define a helpful function for running queries
pysqldf = lambda q: sqldf(q, globals())

# 3. Write the SQL query Impact of income level on defaulting
query = """
SELECT
    CASE
        WHEN AMT_INCOME_TOTAL < 50000 THEN 'Low'
        WHEN AMT_INCOME_TOTAL < 100000 THEN 'Medium'
        ELSE 'High'
    END AS income_bracket,
    COUNT(*) AS total_loans,
    SUM(TARGET) AS total_defaults,
    ROUND(100.0 * SUM(TARGET) / COUNT(*), 2) AS default_rate
FROM df
GROUP BY income_bracket
ORDER BY income_bracket;
"""

# Execute and view the results
result = pysqldf(query)
print(result)

