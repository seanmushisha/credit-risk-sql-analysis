#Impact of age on default rate
query = """
SELECT
    CASE
        WHEN -DAYS_BIRTH / 365.0 BETWEEN 20 AND 30  THEN 'Young'
        WHEN -DAYS_BIRTH / 365.0 BETWEEN 31 AND 40  THEN 'Middle-aged'
        WHEN -DAYS_BIRTH / 365.0 BETWEEN 41 AND 50  THEN 'Mature'
        ELSE 'Senoir'
    END AS Age,
    COUNT(*) AS total_loans,
    SUM(TARGET) AS total_defaults,
    ROUND(100.0 * SUM(TARGET) / COUNT(*), 2) AS default_rate
FROM df
GROUP BY Age
ORDER BY Age;
"""

#Execute and view the results
result = pysqldf(query)
print(result)

#Impact of gender on default rate
query = """
SELECT
    CODE_GENDER,
    COUNT(*) AS total_loans,
    SUM(TARGET) AS total_defaults,
    ROUND(100.0 * SUM(TARGET) / COUNT(*), 2) AS default_rate
FROM df
WHERE CODE_GENDER IN ('M', 'F')
GROUP BY CODE_GENDER
ORDER BY default_rate DESC;
"""
#Execute and view the results
