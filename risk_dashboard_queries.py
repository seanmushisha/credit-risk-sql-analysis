#showing the trend of defaults by age
query= """
SELECT
    CASE
        WHEN -DAYS_BIRTH / 365.0 BETWEEN 20 AND 30 THEN '20-30'
        WHEN -DAYS_BIRTH / 365.0 BETWEEN 31 AND 40 THEN '31-40'
        WHEN -DAYS_BIRTH / 365.0 BETWEEN 41 AND 50 THEN '41-50'
        ELSE '51+'
    END AS age_group,
    COUNT(*) AS total_loans,
    SUM(TARGET) AS total_defaults,
    ROUND(100.0 * SUM(TARGET) / COUNT(*), 2) AS default_rate
FROM application
WHERE DAYS_BIRTH IS NOT NULL
GROUP BY age_group
ORDER BY age_group;
"""
# Execute and view the results
result = pysqldf(query)
