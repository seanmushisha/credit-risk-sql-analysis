#Query 3: Income × Age Risk GridPurpose: Show default rates across income and age segments (heatmap for dashboard)
query = """

SELECT
    CASE
        WHEN AMT_INCOME_TOTAL < 50000 THEN 'Low'
        WHEN AMT_INCOME_TOTAL BETWEEN 50000 AND 99999 THEN 'Medium'
        WHEN AMT_INCOME_TOTAL BETWEEN 100000 AND 149999 THEN 'High'
        ELSE 'Very High'
    END AS income_group,

    -- Age group: split into 4 age ranges using DAYS_BIRTH (converted to years)
    CASE
        WHEN -DAYS_BIRTH / 365.0 BETWEEN 18 AND 30 THEN 'Young'
        WHEN -DAYS_BIRTH / 365.0 BETWEEN 31 AND 40 THEN 'Mid'
        WHEN -DAYS_BIRTH / 365.0 BETWEEN 41 AND 50 THEN 'Mature'
        ELSE 'Senior'
    END AS age_group,

    -- Portfolio size in that segment
    COUNT(*) AS total_loans,

    -- Number of defaults in that segment
    SUM(TARGET) AS total_defaults,

    -- Default rate as a percentage (rounded to 2 decimals)
    ROUND(100.0 * SUM(TARGET) / COUNT(*), 2) AS default_rate

FROM application

-- Limit to realistic working age (18–70) to avoid outliers
WHERE -DAYS_BIRTH / 365.0 BETWEEN 18 AND 70

-- Group by both dimensions to create the grid
GROUP BY income_group, age_group

-- Sort so the grid is easy to read
ORDER BY income_group, age_group;
"""
# Execute and view the results
result = pysqldf(query)
print(result)

