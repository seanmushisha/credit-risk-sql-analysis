# Now query directly from SQLite
query = """
WITH late_flag AS (
    SELECT
        SK_ID_CURR,
        MAX(CASE WHEN DAYS_ENTRY_PAYMENT < 0 THEN 1 ELSE 0 END) AS has_late_payment
    FROM installment
    GROUP BY SK_ID_CURR
),
age_calc AS (
    SELECT
        SK_ID_CURR,
        TARGET,
        AMT_INCOME_TOTAL,
        AMT_CREDIT,
        -DAYS_BIRTH / 365.0 AS age_years
    FROM application
)
SELECT
    a.SK_ID_CURR,
    a.AMT_INCOME_TOTAL AS income,
    a.AMT_CREDIT AS loan_amount,
    ROUND(a.age_years, 1) AS age,
    CASE
        WHEN l.has_late_payment = 1 THEN 'Yes'
        ELSE 'No'
    END AS has_late_payment,
    CASE
        WHEN a.AMT_INCOME_TOTAL < 40000 AND a.age_years < 30 THEN 'Yes'
        WHEN a.AMT_CREDIT > 150000 THEN 'Yes'
        WHEN l.has_late_payment = 1 THEN 'Yes'
        ELSE 'No'
    END AS high_risk_flag
FROM age_calc a
LEFT JOIN late_flag l ON a.SK_ID_CURR = l.SK_ID_CURR
WHERE a.age_years BETWEEN 18 AND 70
ORDER BY high_risk_flag DESC, a.SK_ID_CURR
"""

# Run query directly from SQLite
result = pd.read_sql(query, conn)

# Save to CSV
result.to_csv('high_risk.csv', index=False)

